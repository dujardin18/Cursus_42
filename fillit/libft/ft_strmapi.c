#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	i;
	unsigned int	size;
	char			*tab;

	i = 0;
	size = 0;
	if (!s)
		return (0);
	while (s[size])
		size++;
	if (!(tab = (char *)malloc(sizeof(char) * size + 1)))
		return (NULL);
	while (s[i])
	{
		tab[i] = f(i, s[i]);
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
